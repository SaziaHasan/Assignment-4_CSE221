"""C. Longest Subarray Sum"""

n, k = input().split()
n = int(n)
k = int(k)
lt = input().split()
lt = [int(i) for i in lt]

left = 0
sum = 0
count = 0

for i in range (n):
    sum += lt[i]

    while sum > k and left <= i:
        sum -= lt[left]
        left += 1

    if sum <= k:
        count = max(count, i - left + 1)

print(count)

