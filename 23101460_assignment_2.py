n, s = input().split()
n = int(n)
s = int(s)
lt = input().split()
lt = [int(i) for i in lt]

left = 0
right = n - 1
found = False

while left < right:
    if lt[left] + lt[right] == s:
        print(left + 1, right + 1)
        found = True
        break
    elif lt[left] + lt[right] < s:
        left += 1
    else:
        right -= 1

if not found:
    print("-1")

